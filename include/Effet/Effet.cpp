/****************************************************************************************************/

#include "Effet.hpp"

/****************************************************************************************************/

/****************************************************************************************************/
/**********          Constructeur                                                          **********/
/****************************************************************************************************/


Effet::Effet(int valeur, ComportementEffet * c): val(valeur),cible(NULL), comp(c){
}

/****************************************************************************************************/
/**********          Destructeur                                                           **********/
/****************************************************************************************************/

Effet::~Effet(){
	if(comp!=NULL){
		delete comp;
	}
}

/****************************************************************************************************/

void Effet::prendEffet(){
	comp->comp(cible,val);
}

/****************************************************************************************************/
/********** Accesseurs **********/
/****************************************************************************************************/

void Effet::setCible(Personnage * p){
	cible=p;
}

int Effet::getVal(){
	return val;
}

void Effet::setVal(int n){
	val=n;
}
