/****************************************************************************************************/

#include "Carte.hpp"

/****************************************************************************************************/


/****************************************************************************************************/
/**********          Constructeur                                                          **********/
/****************************************************************************************************/


Carte::Carte(std::string n, Effet * e):nom(n),e(e) {}


Carte::Carte(std::string n, std::string d, Effet * e):nom(n),description(d),e(e) {}


/****************************************************************************************************/
/**********          Destructeur                                                           **********/
/****************************************************************************************************/

Carte::~Carte() {
	if(e!=NULL){
		delete e;
	}
}


/****************************************************************************************************/
/**********          Accesseurs                                                            **********/
/****************************************************************************************************/



std::string Carte::Getnom(){
  return nom;
}


std::string Carte::Getdescription(){
  return description;
}

Effet * Carte::getEffet(){
	return e;
}

void Carte::setEffet(Effet * e){
	this->e=e;
}

/**************************************************************************************************************/

bool Carte::compare(Carte * e){
	if(this->nom==e->Getnom()){
		return true;
	}
	return false;
}
